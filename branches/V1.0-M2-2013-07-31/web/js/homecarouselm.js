function mycarousel_mir_initCallback_mir(carousel_mir)
{
    carousel_mir.buttonNext.bind('click', function() {
        carousel_mir.startAuto(15);
    });

    carousel_mir.buttonPrev.bind('click', function() {
        carousel_mir.startAuto(15);
    });

    // Pause autoscrolling if the user moves with the cursor over the clip.
    carousel_mir.clip.hover(function() {
        carousel_mir.stopAuto(15);
    }, function() {
        carousel_mir.startAuto(15);
    });
};

